    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if (jTable2.getSelectedRow() != -1){  
            Curso c = new Curso();
            CursoDAO cd = new CursoDAO();
            c.setCodigo((int) jTable2.getValueAt(jTable2.getSelectedRow(), 0));
            cd.deleta(c);
            readJTable();
            LimpaText();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        
        if (jTable2.getSelectedRow() != -1){
            jTextField1.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 1).toString());
            jTextArea1.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable2.getSelectedRow() != -1){    
            Curso c = new Curso();
            CursoDAO cd = new CursoDAO();
            c.setDescricao(jTextField1.getText());
            c.setEmenta(jTextArea1.getText());
            c.setCodigo((int) jTable2.getValueAt(jTable2.getSelectedRow(), 0));
            cd.atualiza(c);
            readJTable();
            LimpaText();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


