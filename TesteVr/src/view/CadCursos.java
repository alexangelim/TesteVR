    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setNumRows(0);
        CursoDAO cd = new CursoDAO();
        
        for (Curso c: cd.read()) { 
            modelo.addRow(new Object[]{
                    c.getCodigo(),
                    c.getDescricao(),
                    c.getEmenta()
            });
        }    
    }
    
