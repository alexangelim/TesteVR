    public void readJTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        AlunoDAO ad = new AlunoDAO();
        
        for (Aluno a: ad.read()) { 
            modelo.addRow(new Object[]{
                    a.getCodigo(),
                    a.getNome()
            });
        }
        
    }
    
    public void readJTableMat(){
    
        DefaultTableModel modelo = (DefaultTableModel) jTabMat.getModel();
        modelo.setNumRows(0);
        Aluno a = new Aluno();
        a.setCodigo((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
        MatriculaDAO md = new MatriculaDAO();
        
        for (Matricula m: md.read(a)) { 
            modelo.addRow(new Object[]{
                    m.getCodigo(),
                    m.getDecricao(),
                    m.getEmenta()
            });
        }
    }

