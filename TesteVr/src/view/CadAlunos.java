    public void CarregaCombo(){
        
        CursoDAO cd = new CursoDAO();
        jComboBox1.removeAllItems();
        for (Curso c: cd.read()) {
               jComboBox1.addItem(c);
        } 
        
    }
    
    public void CarregaCombo(Aluno a){
        
        CursoDAO cd = new CursoDAO();
        jComboBox1.removeAllItems();
        for (Curso c: cd.readNotIn(a)) {
               jComboBox1.addItem(c);
        }
    }
    
    
